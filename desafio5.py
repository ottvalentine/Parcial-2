print("=== ÁREA DO TRIÂNGULO ===\n")

print("Passo 1: Digite o valor da base")
base = float(input("→ Base: "))

print("\nPasso 2: Digite o valor da altura")
altura = float(input("→ Altura: "))

print("\nPasso 3: Calculando...")

area = (base * altura) / 2

print(f"\nResultado: A área do triângulo é {area}")